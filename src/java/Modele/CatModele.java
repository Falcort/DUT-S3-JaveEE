package Modele;

public class CatModele {

    int idCat;
    String photoCat;
    String raceCat;
    int priceCat;

    public int getIdCat() {
        return idCat;
    }

    public String getPhotoCat() {
        return photoCat;
    }

    public String getRaceCat() {
        return raceCat;
    }

    public int getPriceCat() {
        return priceCat;
    }

    public void setIdCat(int idCat) {
        this.idCat = idCat;
    }

    public void setPhotoCat(String photoCat) {
        this.photoCat = photoCat;
    }

    public void setRaceCat(String raceCat) {
        this.raceCat = raceCat;
    }

    public void setPriceCat(int priceCat) {
        this.priceCat = priceCat;
    }


}