package Modele;

public class ChienModele {

    int idDog;
    String photoDog;
    String raceDog;
    int priceDog;

    public int getIdDog() {
        return idDog;
    }

    public String getPhotoDog() {
        return photoDog;
    }

    public String getRaceDog() {
        return raceDog;
    }

    public int getPriceDog() {
        return priceDog;
    }

    public void setIdDog(int idDog) {
        this.idDog = idDog;
    }

    public void setPhotoDog(String photoDog) {
        this.photoDog = photoDog;
    }

    public void setRaceDog(String raceDog) {
        this.raceDog = raceDog;
    }

    public void setPriceDog(int priceDog) {
        this.priceDog = priceDog;
    }
}
